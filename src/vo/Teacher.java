package vo;

public class Teacher {
	private String teacherId ;
	private String uploadedPictureName;
	private String originalPictureName;
	private String yakRyeok;
	private String hanMaDi ;
	public String getTeacherId() {
		return teacherId;
	}
	public void setTeacherId(String teacherId) {
		this.teacherId = teacherId;
	}
	public String getUploadedPictureName() {
		return uploadedPictureName;
	}
	public void setUploadedPictureName(String uploadedPictureName) {
		this.uploadedPictureName = uploadedPictureName;
	}
	public String getOriginalPictureName() {
		return originalPictureName;
	}
	public void setOriginalPictureName(String originalPictureName) {
		this.originalPictureName = originalPictureName;
	}
	public String getYakRyeok() {
		return yakRyeok;
	}
	public void setYakRyeok(String yakRyeok) {
		this.yakRyeok = yakRyeok;
	}
	public String getHanMaDi() {
		return hanMaDi;
	}
	public void setHanMaDi(String hanMaDi) {
		this.hanMaDi = hanMaDi;
	}
	
	
	
	
}
